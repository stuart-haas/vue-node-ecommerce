<template>
  <div class="column">
    <div class="card">
      <div class="card-content">
        <div class="content">
          <p class="title is-4">{{ product.name }}</p>
          <p class="subtitle is-6">SKU: {{ product.sku }}</p>
          <p>Description: {{ product.description }}</p>
          <p>Price: ${{ product.price }}</p>
          <p>In Stock: {{ product.inStock }}</p>
          <input class="input is-rounded" type="text" placeholder="Quantity" v-model="quantity">
        </div>
      </div>
      <footer class="card-footer">
        <a href="#" class="card-footer-item" @click="addToCart">Add to Cart</a>
      </footer>
    </div>
  </div>
</template>

<script>
import axios from 'axios'
export default {
  props: {
    product: {
      type: Object
    },
    index: {
      type: Number
    }
  },
  data() {
    return {
      quantity: 0
    }
  },
  methods: {
    addToCart() {
      axios.post('/api/cart/add', null, { params: {
        sku: this.product.sku,
        quantity: this.quantity,
        price: this.product.price
      }}).then(response => {
        console.log(response.data)
      }).catch(error => {
        console.log(error)
      })
    }
  }
}
</script>

